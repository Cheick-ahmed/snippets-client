<template>
	<div class="min-h-screen flex items-center justify-center bg-gray-50 -mt-32 py-12 px-4 sm:px-6 lg:px-8">
		<div class="max-w-md w-full">
			<div>
				<h2 class="mt-6 font-header text-center text-4xl leading-9 font-extrabold text-gray-300">
					Sign in.
				</h2>
			</div>
			<form class="mt-8" @submit.prevent="submit">
				<div class="rounded-md shadow-sm">
					<div>
						<input aria-label="Email address" name="email" type="email" class="appearance-none rounded-none relative block w-full px-3 py-2 border border-gray-300 placeholder-gray-500 text-gray-900 rounded-t-md focus:outline-none focus:shadow-outline-blue focus:border-blue-300 focus:z-10 sm:text-sm sm:leading-5" placeholder="Email address" :class="{ 'border-red-500 transition-all transition-250' : validation.email }" v-model="form.email"/>
					</div>
					<div class="-mt-px">
						<input aria-label="Password" name="password" type="password" class="appearance-none rounded-none relative block w-full px-3 py-2 border border-gray-300 placeholder-gray-500 text-gray-900 rounded-b-md focus:outline-none focus:shadow-outline-blue focus:border-blue-300 focus:z-10 sm:text-sm sm:leading-5" placeholder="Password" :class="{ 'border-red-500 transition-all transition-250' : validation.password }" v-model="form.password" />
					</div>
				</div>
				<template v-if="validation.email">
					<p class="mt-2  text-sm leading-5 font-semibold text-red-500 max-w transition duration-700">
						{{ validation.email[0] }}
					</p>
				</template>
				<div class="mt-6">
					<button type="submit" class="group relative w-full flex justify-center py-2 px-4 border border-transparent text-sm font-medium rounded-md text-white bg-gray-700 hover:bg-gray-800 focus:outline-none focus:border-gray-700 focus:shadow-outline-gray active:bg-gray-700 transition duration-300 ease-in-out">
						<span class="absolute left-0 inset-y pl-3">
							<svg class="h-5 w-5 text-gray-500 group-hover:text-gray-400 transition ease-in-out duration-150" fill="currentColor" viewBox="0 0 20 20">
								<path fill-rule="evenodd" d="M5 9V7a5 5 0 0110 0v2a2 2 0 012 2v5a2 2 0 01-2 2H5a2 2 0 01-2-2v-5a2 2 0 012-2zm8-2v2H7V7a3 3 0 016 0z" clip-rule="evenodd" />
							</svg>
						</span>
						Sign in
					</button>
				</div>
			</form>
			<div class="mt-8 text-center">
				<p class="mt-2  text-sm leading-5 font-semibold text-gray-300 max-w transition duration-700">
					Not have account ? <nuxt-link :to="{name : 'auth-register'}"> Register here </nuxt-link>
				</p>
			</div>
		</div>
	</div>
</div>
</div>
</div>
</template>

<script>
	export default {
		head() {
			return {
				title : 'Connexion'
			}
		},
		data () {
			return {
				form : {
					email : null,
					password : null
				},
				validation : {}
			}
		},

		methods : {
			async submit() {
				try {
					await this.$auth.loginWith('local' , {
						data : this.form
					})
				} catch (e) {
					if (e.response.status === 422) {
						this.validation = e.response.data.errors					}
					}
				}
			}
		}
	</script>